# 8) Faça uma calculadora em python que lê 2 números inteiros e exibe as 4 operações básicas entre eles: soma, subtração, multiplicação, divisão.

n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

print("Soma: {}".format(n1 + n2))
print("Subtração: {}".format(n1 - n2))
print("Divisão: {}".format(n1 / n2))
print("Multiplicação: {}".format(n1 * n2))